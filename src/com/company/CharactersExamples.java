package com.company;

public class CharactersExamples {

    public static void main(String[] args) {

        System.out.print('\t'); // makes a tab
        System.out.print('a');  // prints 'a'
        System.out.print('\n'); // goes to the new line
        System.out.print('c');  // prints 'c'

        System.out.println();


    }
}
